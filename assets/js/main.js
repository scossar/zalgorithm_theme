// conditionally load katex-css on search page
document.body.addEventListener("htmx:afterSwap", function (evt) {
  if (evt.detail.target.querySelector(".katex")) {
    loadKaTexCSS();
  }
});

function loadKaTexCSS() {
  if (document.getElementById("katex-css")) return;

  const link = document.createElement("link");
  link.id = "katex-css";
  link.rel = "stylesheet";
  link.href = "https://cdn.jsdelivr.net/npm/katex@0.16.25/dist/katex.min.css";
  link.integrity =
    "sha384-WcoG4HRXMzYzfCgiyfrySxx90XSl2rxY5mnVY5TwtWE6KLrArNKn0T/mOgNL0Mmi";
  link.crossOrigin = "anonymous";
  document.head.appendChild(link);
}
